def is_nearly_sorted(arr):
    if len(arr) < 2:
        return True

    disorder_count = 0
    for i in range(len(arr) - 1):
        if arr[i] > arr[i + 1]:
            disorder_count += 1

    return disorder_count <= max(1, len(arr) // 10)

def has_many_duplicates(arr):
    if len(arr) == 0:
        return False

    unique_ratio = len(set(arr)) / len(arr)
    return unique_ratio < 0.6

def choose_sorting_algorithm(arr):
    n = len(arr)

    if n <= 10:
        return "Insertion Sort", insertion_sort

    elif is_nearly_sorted(arr):
        return "Insertion Sort", insertion_sort

    elif has_many_duplicates(arr):
        return "Merge Sort", merge_sort

    elif n > 100:
        return "Quick Sort", quick_sort

    else:
        return "Heap Sort", heap_sort

def smart_sort(arr):
    algorithm_name, algorithm_function = choose_sorting_algorithm(arr)
    sorted_arr = algorithm_function(arr)
    return algorithm_name, sorted_arr

